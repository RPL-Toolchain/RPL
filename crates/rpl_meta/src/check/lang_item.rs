macro_rules! lang_items {
    ($($name:ident),* $(,)?) => {
        // static LANG_ITEMS: &[&str] = &[$(
        //     stringify!($name),
        // )*];
        pub(crate) fn is_lang_item(name: &str) -> bool {
            match name {
                $(stringify!($name) => true,)*
                _ => false,
            }
        }
    };
}

